using System;

namespace MathXY
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Maximum: " + Math.Max(23,66));
            Console.WriteLine("Minimum: " + Math.Min(23,66));
            Console.WriteLine("Squire Root: " + Math.Sqrt(64));
            Console.WriteLine("Absolute Value of -4.7: " + Math.Abs(-4.7));
            Console.WriteLine("Round off 99.99: " + Math.Round(99.99));
        }
    }
}
